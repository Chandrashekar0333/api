let cards = document.getElementById("card-container");

function displayProducts(filteredProducts) {
  cards.innerHTML = ''; 
  filteredProducts.forEach(product => {
    let card = document.createElement('div');
    card.className = 'card';

    let img = document.createElement('img');
    img.src = product.image;
    img.alt = product.title;
    img.width = 200;
    img.height = 200;
    card.appendChild(img);

    let title = document.createElement('h2');
    title.textContent = product.title;
    card.appendChild(title);

    let description = document.createElement('p');
    description.textContent = product.description;
    card.appendChild(description);

    let price = document.createElement('p');
    price.textContent = `price: ${product.price}`;
    price.id = "price";
    card.appendChild(price);

    let rating = document.createElement('p');
    rating.id = 'rating';
    rating.textContent = `  ${product.rating.rate}`;
    card.appendChild(rating);

    let reviews = document.createElement("span");
    reviews.textContent = ` (${product.rating.count} reviews)`;
    card.appendChild(reviews);

    let button = document.createElement('button');
    button.textContent = 'Add to Cart';
    button.id = 'add-to-cart';
    card.appendChild(button);

    cards.appendChild(card);
  });
}

displayProducts(products);

let searchInput = document.getElementById('search');
searchInput.addEventListener('input', function() {
  let searchTerm = searchInput.value.toLowerCase();
  let filteredProducts = products.filter(product => product.title.toLowerCase().includes(searchTerm));
  displayProducts(filteredProducts);
});           
